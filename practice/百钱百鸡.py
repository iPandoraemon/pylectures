'''
14、使用循环和列表推导式两种方法求解百钱买百鸡问题。
假设大鸡5元一只, 中鸡3元一只, 小鸡1元三只, 现有100元钱
想买100只鸡, 有多少种买法？

设大鸡x，中y，小鸡z
5*20=100，最多可以买20只大鸡；最多可买33只中鸡；最多可以买100只小鸡
'''
for x in range(21):   # 0, 1, 2, ..., 20只大鸡
	for y in range(34): # 0-33只中鸡
		z = 100-x-y     # 小鸡：除了x只大鸡和y只中鸡
		if z%3==0 and 5*x + 3*y + z//3 == 100:
			print(x,y,z)

# 推导式
[(x, y, 100-x-y) for x in range(21) for y in range(34) if (100-x-y)%3==0 and 5*x+3*y+(100-x-y)//3==100]